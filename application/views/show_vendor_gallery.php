     function display_vendors()
        {
            $.ajax
                ({
                    type: "POST",
                    url: "<?php echo base_url(); ?>"+"Vendor_Gallery/get_vendors",
                    dataType: 'json',                                   
                    success: function (data)
                      { // alert(JSON.stringify(data));
					
                        $("<option />").val("0")
                                    .text("Select Vendor")
                                    .appendTo($('select#Vendor_list'));
                        for(var i=0;i<data.length;i++)
                            { 
                                $("<option />").val(data[i].Vendor_Id)
                                              .text(data[i].Name)
                                              .appendTo($('select#Vendor_list'));
                            }

                      }

                });
        }
	  
        $('#Vendor_list').on('change',function()
        {  
             
             var id = $(this).val();
         var    data_name = []; 
             $.ajax
                ({
                     type: "get",
                    url: "<?php echo base_url(); ?>Vendor_Gallery/get_product_name/"+id,
                    data:"ven_id="+id,
                    dataType: 'json', 
                    success: function (res){
                     if(res){
					  for(var k1=0;k1<res.length;k1++){                                            
                                             data_name.push({name:res[k1].product_version_name,id:res[k1].product_version_id});
                                           
                                          }
                                             show_image(data_name,id);
					}
                                        }
                });
               
                });
                function show_image(array,id){

              $('#img_data').empty();
             
              $.ajax
                ({
                     type: "get",
                    url: "<?php echo base_url(); ?>Vendor_Gallery/vendor_gallery_img/"+id,
                    data:"ven_id="+id,
                    dataType: 'json', 
                    success: function (res){
						
						var arr=res.im;
						var len=arr.length;
						  for(var i=0;i<len;i++){
                                                    
                        var data_id=res.dir[i];                                                   
                         var   delimiter = '/';
                         var start = 3;
                         var tokens = data_id.split(delimiter).slice(start);
                         var result = tokens.join(delimiter);
                         var id_service = result;                         
                        id_service = id_service.substring(0, id_service.indexOf('/'));
                        var con="";					
                       con +='<div class="col-lg-3 col-md-4 col-sm-6">';
                       con +='<div class="thumbnail-box">';
                       for(var gn=0;gn<array.length;gn++){
                           if(array[gn].id==id_service){
                             con +='<div class="thumb-content">';
                        con +='<div class="center-vertical">';
                        con +='<div class="center-content">';
                         con +='<h3 class="thumb-heading animated rollIn">';
                         con +=array[gn].name;
                         con +='</h3>';
                         con +='</div>';   
                          con +='</div>';   
                           con +='</div>'; 
                           }
                       }
                      
                     
                       con +='<div class="thumb-overlay bg-blue"></div>';
                        con +='<img id="imgSmile_'+i+'" class="myImg"  src="'+res.im[i]+'" alt="">';
